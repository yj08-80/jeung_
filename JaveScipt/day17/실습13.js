function 연락처등록함수(){
    console.log("---연락처등록함수 실행중---");
    const nameBtn = document.querySelector( "#nameBtn" );
        console.log( nameBtn );
    const telBtn = document.querySelector( "#telBtn" );
        console.log( telBtn );
    const textBtn = document.querySelector( "#textBtn" );
        console.log( textBtn );
    
    const name = nameBtn.value;
        console.log( name );
    const tel = telBtn.value;
        console.log( tel );
    const text = textBtn.value;
        console.log( text );
    const obj = { 코드 : 3 , 이름 : name , 전화번호 : tel , 메모 : text };
    console.log( obj );
    연락처목록.push( obj );
    console.log( 연락처목록 );
    연락처목록함수();
}
